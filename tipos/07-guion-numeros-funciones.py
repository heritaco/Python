import math

print(round(1.3))
print(round(1.7))
print(round(1.5))
print(round(1.49999999999999))  # redonea

# valor absoluto
print(abs(-2525))

# valor entero mas cercano
print(math.ceil(2.01))
print(math.ceil(2))

# funcion piso
print(math.floor(2))
print(math.floor(1.9))

print(math.isnan(23))
# print(math.isnan("23")) solo funciona con numeros reales

print(math.pow(3, 3))
print(math.sqrt(9))

